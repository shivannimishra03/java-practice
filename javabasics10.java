import java.util.*;

public class javabasics10 {
    public static void main(String[] args) {
        //type casting (explicit conversion)
        Scanner sc = new Scanner(System.in);
        float a = 25.99999f;
        int b = (int) a;
        System.out.println(b);

        char ch = 'a';
        char ch2 = 'b';
        int number = ch;
        int number2 = ch2;
        System.out.println(number);
        System.out.println(number2);

    }
    
}
