public class operator2 {
    public static void main(String[] args) {
        //Relational Operator
        int A = 10;
        int B = 5;
        System.out.println(A==B);
        System.out.println(A!=B);
        System.out.println(A<B);
        System.out.println(A>B);
        System.out.println(A>=B);
        System.out.println(A<=B);
    }
    
}
