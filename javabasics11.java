public class javabasics11 {
    public static void main(String[] args) {
        //type promotions in expression
        char a = 'a';
        char b = 'b';
        System.out.println((int)(b));
        System.out.println((int)(a));
        System.out.println(a);
        System.out.println(b-a);
    }
    
}
