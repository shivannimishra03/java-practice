public class operator4 {
    public static void main(String[] args) {
        //Assiment operator 

        int A = 10;
        //A = A + 10;
        A += 10 ;

        int B = 5;
        //B = B - 5;
        B -= 5;
        System.out.println(B);

        int C = 5;
        //C = B * 5;
        C *= 5;
        System.out.println(C);

        int D = 5;
        //D = B / 5;
        D /= 5;
        System.out.println(D);



    }
    
}
