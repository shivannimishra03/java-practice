import java.util.*;

public class operator3 {
    public static void main(String[] args) {
        //Logical Operator 

    
        System.out.println(( 3 > 2 ) && ( 5 < 0 ));   //And operator

        System.out.println (( 3 > 2 ) || ( 5 > 0 ));

        System.out.println ( !( 3 > 2 ));

    }
    
}
